<P>General printing setup for blank 5x8 index cards (office depot, staples, etc).  At least what has worked for me.</P>

<P>Very first time (Windows):</P>
<ul>
<li>Open your printers folder
<li>Select printer properties for desired printer
<li>Click "Printing Preferences"
<li>Find the paper source section
<li>Create a custom paper source of size 5" width x 8" length
<li>Name it indexcard or what ever you like
</ul>

<P>Print cards</P>
<ul>
<li>Download the PDF file of cards (pick the ordering you want)
<li>Open in Acrobat Reader and Click print
<li>In the preferences dialog:
<ul>
<li>Set Page Scaling to "None"
<li>Select "Choose Paper Source by PDF page size"
</ul>
<li>Print
</ul>

<P>
Note that you want to feed in index cards in length wise just like regular paper.  I would test print just one page (one card) to start with to see if things work correctly
</P>
